# Example list
my_list = [1, 2, 3, 4, 1, 2, 1, 3, 4, 5]

# Element to count occurrences of
element = int(input("Enter the element to count occurrences: "))

# Count occurrences of the element in the list
occurrences = my_list.count(element)

print(f"The element {element} occurs {occurrences} times in the list.")
