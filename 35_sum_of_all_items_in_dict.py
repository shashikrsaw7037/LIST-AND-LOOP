# 35. Write a Python program to sum all the items in a dictionary.


my_dict = {'a': 10, 'b': 20, 'c': 30, 'd': 40}
sum = 0
for i in my_dict.values():
    sum+=i
print("sum all the items in a dictionary",sum)




































